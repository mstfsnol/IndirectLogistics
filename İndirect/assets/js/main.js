const STORAGE_KEY = 'indirect-logistics-lang';
const FALLBACK_LANG = 'tr';

function getDefaultLang() {
  return document.documentElement.dataset.defaultLang || FALLBACK_LANG;
}

function getPageKey() {
  return document.body?.dataset.page || 'home';
}

function buildTranslationMap(lang) {
  const fallbackData = translations[FALLBACK_LANG] || {};
  const activeData = translations[lang] || {};
  const page = getPageKey();
  return {
    ...(fallbackData.common || {}),
    ...(fallbackData[page] || {}),
    ...(activeData.common || {}),
    ...(activeData[page] || {})
  };
}

let currentLang = null;

function updateLanguageButtons(lang) {
  document.querySelectorAll('.lang-btn').forEach(btn => {
    const isActive = btn.dataset.lang === lang;
    btn.classList.toggle('is-active', isActive);
    btn.setAttribute('aria-pressed', String(isActive));
  });
}

function applyTranslations(lang) {
  const targetLang = translations[lang] ? lang : FALLBACK_LANG;
  const map = buildTranslationMap(targetLang);
  currentLang = targetLang;
  document.documentElement.lang = targetLang;

  try {
    localStorage.setItem(STORAGE_KEY, targetLang);
  } catch (err) {
    // storage not available, ignore
  }

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (!key) return;
    const value = map[key];
    if (value === undefined) return;

    const hasAttrOnly = !!el.dataset.i18nAttr && !el.dataset.i18nText;
    if (!hasAttrOnly) {
      el.innerHTML = value;
    }
  });

  document.querySelectorAll('[data-i18n-attr]').forEach(el => {
    const attr = el.dataset.i18nAttr;
    const key = el.dataset.i18n;
    if (!attr || !key) return;
    const value = map[key];
    if (value !== undefined) {
      el.setAttribute(attr, value);
    }
  });

  const attrMappings = [
    { selector: '[data-i18nPlaceholder]', attr: 'placeholder', key: 'i18nPlaceholder' },
    { selector: '[data-i18nValue]', attr: 'value', key: 'i18nValue' },
    { selector: '[data-i18nTitle]', attr: 'title', key: 'i18nTitle' },
    { selector: '[data-i18nAlt]', attr: 'alt', key: 'i18nAlt' }
  ];

  attrMappings.forEach(({ selector, attr, key }) => {
    document.querySelectorAll(selector).forEach(el => {
      const lookup = el.dataset[key];
      if (!lookup) return;
      const value = map[lookup];
      if (value !== undefined) {
        el.setAttribute(attr, value);
      }
    });
  });

  const titleEl = document.querySelector('title[data-i18n]');
  if (titleEl) {
    document.title = titleEl.textContent;
  }

  updateLanguageButtons(targetLang);
}

function initLanguageSwitcher() {
  const buttons = document.querySelectorAll('.lang-btn');
  if (!buttons.length) return;

  buttons.forEach(btn => {
    btn.addEventListener('click', () => {
      const lang = btn.dataset.lang;
      if (!lang || lang === currentLang) return;
      applyTranslations(lang);
    });
  });
}

function initNavigationToggle() {
  const navToggle = document.querySelector('.nav-toggle');
  const navId = navToggle?.dataset.target;
  const nav = navId ? document.getElementById(navId) : document.querySelector('.site-nav');

  if (!navToggle || !nav) return;

  navToggle.addEventListener('click', () => {
    const isOpen = nav.classList.toggle('open');
    navToggle.classList.toggle('is-active', isOpen);
    navToggle.setAttribute('aria-expanded', String(isOpen));
  });

  nav.querySelectorAll('a').forEach(link => {
    link.addEventListener('click', () => {
      if (window.innerWidth <= 900) {
        nav.classList.remove('open');
        navToggle.classList.remove('is-active');
        navToggle.setAttribute('aria-expanded', 'false');
      }
    });
  });

  window.addEventListener('resize', () => {
    if (window.innerWidth > 900) {
      nav.classList.remove('open');
      navToggle.classList.remove('is-active');
      navToggle.setAttribute('aria-expanded', 'false');
    }
  });
}

function initHeaderScrollEffect() {
  const header = document.querySelector('.site-header');
  if (!header) return;

  const onScroll = () => {
    if (window.scrollY > 8) {
      header.classList.add('is-scrolled');
    } else {
      header.classList.remove('is-scrolled');
    }
  };

  onScroll();
  window.addEventListener('scroll', onScroll, { passive: true });
}

function initHeroSlider() {
  const slider = document.querySelector('[data-slider]');
  if (!slider) {
    console.log('Slider element not found');
    return;
  }

  const slides = Array.from(slider.querySelectorAll('.slide'));
  const dots = Array.from(slider.querySelectorAll('.slider-dot'));
  const prevBtn = slider.querySelector('[data-slide-dir="prev"]');
  const nextBtn = slider.querySelector('[data-slide-dir="next"]');
  let index = 0;
  let timerId = null;
  const interval = 7000;

  console.log('Slider initialized with', slides.length, 'slides');

  // Check if images are loading
  slides.forEach((slide, idx) => {
    const img = slide.querySelector('img');
    if (img) {
      img.addEventListener('load', () => {
        console.log(`Image ${idx + 1} loaded successfully`);
      });
      img.addEventListener('error', () => {
        console.log(`Image ${idx + 1} failed to load:`, img.src);
      });
    }
  });

  const setActiveSlide = newIndex => {
    if (!slides.length) return;
    const total = slides.length;
    const target = (newIndex + total) % total;

    slides.forEach((slide, idx) => {
      slide.classList.toggle('is-active', idx === target);
    });

    dots.forEach((dot, idx) => {
      const isActive = idx === target;
      dot.classList.toggle('is-active', isActive);
      dot.setAttribute('aria-current', isActive ? 'true' : 'false');
    });

    index = target;
  };

  const startAutoPlay = () => {
    stopAutoPlay();
    timerId = window.setInterval(() => {
      setActiveSlide(index + 1);
    }, interval);
  };

  const stopAutoPlay = () => {
    if (timerId) {
      window.clearInterval(timerId);
      timerId = null;
    }
  };

  prevBtn?.addEventListener('click', () => {
    setActiveSlide(index - 1);
    startAutoPlay();
  });

  nextBtn?.addEventListener('click', () => {
    setActiveSlide(index + 1);
    startAutoPlay();
  });

  dots.forEach((dot, dotIndex) => {
    dot.addEventListener('click', () => {
      setActiveSlide(dotIndex);
      startAutoPlay();
    });
  });

  slider.addEventListener('mouseenter', stopAutoPlay);
  slider.addEventListener('mouseleave', startAutoPlay);
  slider.addEventListener('focusin', stopAutoPlay);
  slider.addEventListener('focusout', startAutoPlay);

  // Ensure first slide is visible immediately
  setActiveSlide(0);
  
  // Start autoplay after a short delay to ensure images are loaded
  setTimeout(() => {
    startAutoPlay();
  }, 1000);
}

document.addEventListener('DOMContentLoaded', () => {
  const storedLang = (() => {
    try {
      return localStorage.getItem(STORAGE_KEY);
    } catch (err) {
      return null;
    }
  })();

  const initialLang = storedLang && translations[storedLang] ? storedLang : getDefaultLang();

  applyTranslations(initialLang);
  initLanguageSwitcher();
  initNavigationToggle();
  initHeaderScrollEffect();
  initHeroSlider();
});
